/**
 * indexOf = Encontra qual o indice da primeira ocorrencia de um elemento no array
 * lastIndexOf = Encontra qual o indice da ultima ocorrencia de um elemento no array 
 */

let letras = ["A","B","C","B","C","A","C"];

//Irá retornar 0, pois a primeira ocorrencia de A esta na posição 0 do array
console.log(letras.indexOf("A"));

//Irpa retornar 5, pois a ultima ocorrencia de A esta na posição 5 do array
console.log(letras.lastIndexOf("A"));